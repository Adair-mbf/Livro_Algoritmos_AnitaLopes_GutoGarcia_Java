package Aula03.Algoritmo163;

public class Main {
    public static void main(String[] args) {
        int voltaLagoa;
        int abdominais;

        for(voltaLagoa = 1; voltaLagoa <= 3; voltaLagoa++) {
            System.out.println(voltaLagoa + "ª volta na Lagoa\n");
            for(abdominais =1; abdominais <= 5; abdominais++) {
                System.out.println(abdominais + "º abdominal");
            }
            System.out.print("\n");
        }
        System.out.print("\n");
    }
}
